#include <iostream>
#include <windows.h>

using namespace std;

int main() {
    SetConsoleOutputCP(CP_UTF8);

    // 1. Цикл от 1 до 20
    for (int i = 1; i <= 20; i++) {
        
        // 2. Если число 17 — это финиш (Счастливое число)
        if (i == 17) {
            cout << "Ура, 17! Это моё счастливое число. Останавливаемся!" << endl;
            break; // Выходим из цикла совсем
        }

        // 3. Если число делится на 5 — пропускаем его
        // Оператор % находит остаток от деления. Если он 0 — число делится нацело.
        if (i % 5 == 0) {
            cout << "(Число " << i << " пропущено)" << endl;
            continue; // Прыгаем в начало цикла к следующему числу
        }

        // 4. Печатаем все остальные числа
        cout << "Число: " << i << endl;
    }

    cout << "Цикл завершён!" << endl;

    return 0;
}